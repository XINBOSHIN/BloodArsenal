package com.arc.bloodarsenal.common.items;

import java.util.List;

import net.minecraft.entity.Entity;
import net.minecraft.entity.player.EntityPlayer;
import net.minecraft.item.Item;
import net.minecraft.item.ItemStack;
import net.minecraft.nbt.NBTTagCompound;
import net.minecraft.potion.Potion;
import net.minecraft.potion.PotionEffect;
import net.minecraft.util.StatCollector;
import net.minecraft.world.World;

import WayofTime.alchemicalWizardry.api.items.interfaces.IBindable;
import WayofTime.alchemicalWizardry.common.items.EnergyItems;

import com.arc.bloodarsenal.common.BloodArsenal;

public class ItemSoulBooster extends Item implements IBindable {

    public ItemSoulBooster() {
        super();
        setMaxStackSize(1);
        setUnlocalizedName("soul_booster");
        setTextureName("BloodArsenal:soul_booster");
        setCreativeTab(BloodArsenal.BA_TAB);
    }

    @Override
    public ItemStack onItemRightClick(ItemStack par1ItemStack, World par2World, EntityPlayer par3EntityPlayer) {
        EnergyItems.checkAndSetItemOwner(par1ItemStack, par3EntityPlayer);

        setActivated(par1ItemStack, !getActivated(par1ItemStack));

        if (par3EntityPlayer.isSneaking()) {
            par1ItemStack.stackTagCompound.setInteger("worldTimeDelay", (int) (par2World.getWorldTime() - 1) % 100);
            return par1ItemStack;
        }

        if (!getActivated(par1ItemStack)) {
            return par1ItemStack;
        }

        return par1ItemStack;
    }

    @Override
    public void onUpdate(ItemStack par1ItemStack, World par2World, Entity par3Entity, int par4, boolean par5) {
        if (!(par3Entity instanceof EntityPlayer)) {
            return;
        }

        EntityPlayer par3EntityPlayer = (EntityPlayer) par3Entity;

        if (par1ItemStack.stackTagCompound == null) {
            par1ItemStack.setTagCompound(new NBTTagCompound());
        }

        if (par2World.getWorldTime() % 100 == par1ItemStack.stackTagCompound.getInteger("worldTimeDelay")
                && par1ItemStack.stackTagCompound.getBoolean("isActive")) {
            if (!par3EntityPlayer.capabilities.isCreativeMode) {
                EnergyItems.syphonBatteries(par1ItemStack, par3EntityPlayer, 1000);
            }
        }

        if (par1ItemStack.stackTagCompound.getBoolean("isActive")) {
            par3EntityPlayer.addPotionEffect(new PotionEffect(Potion.moveSpeed.id, 2, 0));
            par3EntityPlayer.addPotionEffect(new PotionEffect(Potion.damageBoost.id, 2, 0));
            par3EntityPlayer.addPotionEffect(new PotionEffect(Potion.jump.id, 2, 0));
            par3EntityPlayer.addPotionEffect(new PotionEffect(Potion.regeneration.id, 2, 0));
            par3EntityPlayer.addPotionEffect(new PotionEffect(Potion.resistance.id, 2, 0));
        }
    }

    public void setActivated(ItemStack par1ItemStack, boolean newActivated) {
        NBTTagCompound itemTag = par1ItemStack.stackTagCompound;

        if (itemTag == null) {
            par1ItemStack.setTagCompound(new NBTTagCompound());
        }

        itemTag.setBoolean("isActive", newActivated);
    }

    public boolean getActivated(ItemStack par1ItemStack) {
        NBTTagCompound itemTag = par1ItemStack.stackTagCompound;

        if (itemTag == null) {
            par1ItemStack.setTagCompound(new NBTTagCompound());
        }

        return itemTag.getBoolean("isActive");
    }

    @Override
    public void addInformation(ItemStack par1ItemStack, EntityPlayer par2EntityPlayer, List par3List, boolean par4) {
        par3List.add(StatCollector.translateToLocal("tooltip.itemba.soul_booster1"));
        par3List.add(StatCollector.translateToLocal("tooltip.itemba.soul_booster2"));

        if (!(par1ItemStack.stackTagCompound == null)) {
            if (par1ItemStack.stackTagCompound.getBoolean("isActive")) {
                par3List.add("Activated");
            } else {
                par3List.add("Deactivated");
            }

            if (!par1ItemStack.stackTagCompound.getString("ownerName").equals("")) {
                par3List.add("Current owner: " + par1ItemStack.stackTagCompound.getString("ownerName"));
            }
        }
    }
}
